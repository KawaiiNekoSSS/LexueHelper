package bit.local.tools;

/**
 * @author lire
 * @title: IgnoreMode
 * @projectName LexueHelper
 * @description: 比对类型
 */

public enum IgnoreMode {
        /**
         * 忽略掉文末空字符
         */
        IGNORE_BLANK_ON_END,
        /**
         * 不忽略空字符
         */
        IGNORE_NO_BLANK,
        /**
         * 忽略所有空字符
         */
        IGNORE_ALL_BLANK
}
